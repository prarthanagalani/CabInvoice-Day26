package CabInvoiceGenerator.src.main.java.org.example;

public class Ride {

    private double distance, time;

    public Ride(double distance, double time) {
        this.distance = distance;
        this.time = time;
    }

    public double getDistance() {
        return distance;
    }

    public double getTime() {
        return time;
    }

}
